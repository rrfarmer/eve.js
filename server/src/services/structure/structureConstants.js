function getAllowedServicesForStructureType(typeID, family = null) {
  const resolvedTypeID = Number(typeID) || 0;
  if (FLEX_SERVICE_IDS_BY_TYPE_ID[resolvedTypeID]) {
    return [...FLEX_SERVICE_IDS_BY_TYPE_ID[resolvedTypeID]];
  }

  const preset = STRUCTURE_TYPE_PRESETS[resolvedTypeID] || null;
  const resolvedFamily = family || (preset && preset.family) || STRUCTURE_FAMILY.UNKNOWN;
  if (resolvedFamily === STRUCTURE_FAMILY.CITADEL) {
    return [...CITADEL_SERVICE_IDS];
  }
  if (resolvedFamily === STRUCTURE_FAMILY.ENGINEERING) {
    return [...ENGINEERING_SERVICE_IDS];
  }
  if (resolvedFamily === STRUCTURE_FAMILY.REFINERY) {
    return [...REFINERY_SERVICE_IDS];
  }
  if (resolvedFamily === STRUCTURE_FAMILY.FLEX) {
    return [...(FLEX_SERVICE_IDS_BY_TYPE_ID[resolvedTypeID] || [])];
  }
  if (resolvedFamily === STRUCTURE_FAMILY.SOV) {
    return [];
  }
  return [
    STRUCTURE_SERVICE_ID.DOCKING,
    STRUCTURE_SERVICE_ID.FITTING,
  ];
}

